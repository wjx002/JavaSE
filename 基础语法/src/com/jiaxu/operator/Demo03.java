public class Demo03 {
    public static void main(String[] args) {
        //关系运算符返回结果：true false 布尔值
        //if
        int a = 10;
        int b = 20;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);

        int c = 21;
        //取余或者叫做模运算
        System.out.println(c % a);
    }
}
